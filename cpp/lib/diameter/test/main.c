bool is_same_avp(FLK_AVP_ST *src, FLK_AVP_ST *dest)
{
	char buf[1024] = {0x00, };
	if ( src->avp_code != dest->avp_code ) {
		return false;
	}

	if ( src->header.avp_code != dest->header.avp_code) 
		return false;

	if ( src->header.opt.val != dest->header.opt.val )
		return false;

	memcpy(buf, octstr_get_cstr(src->data), octstr_len(src->data));

	if ( memcmp(buf, octstr_get_cstr(dest->data), octstr_len(dest->data)) != 0 )
		return false;

	return true;
}
